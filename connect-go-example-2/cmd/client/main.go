package main

import (
	"context"
	"log"
	"net/http"

	greetv1 "example/gen/greet/v1"
	"example/gen/greet/v1/greetv1connect"
)

func main() {
	client := greetv1connect.NewGreetServiceClient(
		http.DefaultClient,
		"http://localhost:8080",
	)
	res, err := client.Greet(
		context.Background(),
		&greetv1.GreetRequest{
			Name: "Bob",
		},
	)
	if err != nil {
		log.Println(err)
		return
	}

	log.Println(res.Greeting)
}
